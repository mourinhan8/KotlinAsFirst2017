/**
 * Простая
 *
 * Для заданных чисел m и n, m <= n, определить, имеется ли хотя бы один точный квадрат между m и n,
 * то есть, существует ли такое целое k, что m <= k*k <= n.
 * Например, для интервала 21..28 21 <= 5*5 <= 28, а для интервала 51..61 квадрата не существует.
 */
fun squareBetweenExists(m: Int, n: Int): Boolean {
    val x = Math.sqrt(m.toDouble()).toInt()
    val y = Math.sqrt(n.toDouble()).toInt()
    for (i in x..y) {
        val k = i.toDouble()
        val q = sqr(k)
        if (q in m..n) return true
    }
    return false
}

/**
 * Средняя
 *
 * Для заданного x рассчитать с заданной точностью eps
 * sin(x) = x - x^3 / 3! + x^5 / 5! - x^7 / 7! + ...
 * Нужную точность считать достигнутой, если очередной член ряда меньше eps по модулю
 */
fun sin(x: Double, eps: Double): Double {
    var sinx = x%(2* PI)
    var i = 1
    val t = sinx
    var numerator = sinx
    var denominator = 1.0
    while (Math.abs(numerator / denominator) > eps) {
        numerator = numerator * t * t * (-1)
        denominator = denominator * (i + 1) * (i + 2)
        sinx += numerator / denominator
        i = i + 2
    }
    return sinx
}

/**
 * Средняя
 *
 * Для заданного x рассчитать с заданной точностью eps
 * cos(x) = 1 - x^2 / 2! + x^4 / 4! - x^6 / 6! + ...
 * Нужную точность считать достигнутой, если очередной член ряда меньше eps по модулю
 */
fun cos(x: Double, eps: Double): Double {
    var cosx = 1.0
    var i = 0.0
    var denominator = 1.0
    val t = x%(2* PI)
    var numerator = 1.0
    if (x == 0.0) return 1.0
    while (abs(numerator / denominator) >= eps) {
        denominator = denominator * (i + 1.0) * (i + 2.0)
        numerator = numerator * t * t * (-1.0)
        cosx = cosx + numerator / denominator
        i = i + 2.0
    }
    return cosx
}

